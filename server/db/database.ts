// ── Bootstrap admin ────────────────────────────────────────────────────────

/**
 * Create the initial admin user and default team on first run.
 * Called from server startup. Idempotent — skips if users already exist.
 * Returns true if bootstrap was performed.
 */
export async function bootstrapAdmin(): Promise<boolean> {
  const db = getDb();
  const userCount = ((await db.prepare('SELECT COUNT(*) as c FROM users').get()) as { c: number }).c;
  if (userCount > 0) return false;

  const email = readSecret('A2N_ADMIN_EMAIL');
  const password = readSecret('A2N_ADMIN_PASSWORD');
  const displayName = process.env.ANALYST_NAME || 'Admin';

  if (!email || !password) {
    logger.warn('\n⚠  No users exist and A2N_ADMIN_EMAIL / A2N_ADMIN_PASSWORD are not set.');
    logger.warn('   Set these in your .env file to create the initial SNR admin account.\n');
    return false;
  }

  // Lazy-import to avoid circular dependency at module load time
  const { hashPassword, validatePasswordStrength } = await import('../lib/auth-utils.js');

  // Validate admin password meets complexity requirements
  const pwCheck = validatePasswordStrength(password);
  if (!pwCheck.valid) {
    logger.error(`\n✗ Admin password does not meet complexity requirements:`);
    for (const err of pwCheck.errors) logger.error(`  - ${err}`);
    logger.error('  Update A2N_ADMIN_PASSWORD in .env and restart SNR.\n');
    return false;
  }

  const now = Date.now();
  const adminId = crypto.randomUUID();
  const teamId = crypto.randomUUID();
  const passwordHash = await hashPassword(password);

  // Create admin user
  await db
    .prepare(
      `INSERT INTO users (id, email, password_hash, display_name, role, created_at, updated_at)
       VALUES (?, ?, ?, ?, 'admin', ?, ?)`
    )
    .run(adminId, email.toLowerCase(), passwordHash, displayName, now, now);

  // Create default team
  await db
    .prepare(
      `INSERT INTO teams (id, name, description, created_at, updated_at)
       VALUES (?, 'Default Team', 'Auto-created during initial setup', ?, ?)`
    )
    .run(teamId, now, now);

  // Add admin as team lead
  await db
    .prepare(
      `INSERT INTO team_members (team_id, user_id, role, joined_at)
       VALUES (?, ?, 'lead', ?)`
    )
    .run(teamId, adminId, now);

  // Migrate existing sessions to the default team
  await db
    .prepare('UPDATE sessions SET team_id = ?, created_by = ? WHERE team_id IS NULL')
    .run(teamId, adminId);

  // Migrate existing audit log entries
  await db.prepare('UPDATE audit_log SET user_id = ? WHERE user_id IS NULL').run(adminId);

  logger.info(`✓ Admin account created: ${email}`);
  logger.info(`✓ Default Team created. All existing sessions assigned.`);

  return true;
}

// ── Settings helpers ───────────────────────────────────────────────────────

// Small in-process cache for merged settings. Settings change rarely (admin
// actions) but are read on every analyze/export/preview, so a short TTL plus
// explicit invalidation on write avoids two DB round-trips per read. Callers get
// a fresh shallow copy so they can't mutate the cached object.
const SETTINGS_TTL_MS = parseInt(process.env.SETTINGS_CACHE_TTL_MS ?? '30000', 10);
const settingsCache = new Map<string, { value: Record<string, string>; exp: number }>();

/** Clear the merged-settings cache (call after any settings write). */
export function invalidateSettingsCache(): void {
  settingsCache.clear();
}

/**
 * Load settings with team-level overrides merged on top of org defaults.
 * If teamId is empty/undefined, returns only global settings.
 */
export async function loadMergedSettings(teamId?: string): Promise<Record<string, string>> {
  const cacheKey = teamId || '__global__';
  const cached = settingsCache.get(cacheKey);
  if (cached && cached.exp > Date.now()) return { ...cached.value };

  const db = getDb();

  // Global (org-wide) settings
  const globalRows = (await db.prepare('SELECT key, value FROM settings').all()) as Array<{
    key: string;
    value: string;
  }>;
  const merged: Record<string, string> = {};
  for (const r of globalRows) merged[r.key] = r.value;

  // Overlay team-specific settings if teamId provided
  if (teamId) {
    const teamRows = (await db
      .prepare('SELECT key, value FROM team_settings WHERE team_id = ?')
      .all(teamId)) as Array<{ key: string; value: string }>;
    for (const r of teamRows) merged[r.key] = r.value;
  }

  settingsCache.set(cacheKey, { value: merged, exp: Date.now() + SETTINGS_TTL_MS });
  return { ...merged };
}

// ── Audit log ──────────────────────────────────────────────────────────────

/**
 * Append an audit-log entry. Best-effort and self-contained: errors are caught
 * and logged rather than thrown, so callers may invoke without awaiting (the
 * append still completes). The audit log is append-only.
 */
export async function appendAuditLog(entry: {
  analyst_name: string;
  user_id?: string;
  session_id?: string;
  action: string;
  input_hash?: string;
  outputs_generated?: string[];
  techniques_identified?: string[];
  details?: string;
}): Promise<void> {
  try {
    const db = getDb();
    await db
      .prepare(
        `INSERT INTO audit_log (timestamp, analyst_name, user_id, session_id, action, input_hash, outputs_generated, techniques_identified, details)
         VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?)`
      )
      .run(
        Date.now(),
        entry.analyst_name,
        entry.user_id ?? null,
        entry.session_id ?? null,
        entry.action,
        entry.input_hash ?? null,
        entry.outputs_generated ? JSON.stringify(entry.outputs_generated) : null,
        entry.techniques_identified ? JSON.stringify(entry.techniques_identified) : null,
        entry.details ?? null
      );
  } catch (err) {
    logger.error({ err, action: entry.action }, 'Failed to append audit-log entry');
  }
}
