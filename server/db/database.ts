// SNR V3 data layer — Postgres (V2 used the synchronous node:sqlite API).
// getDb() returns an async client whose shape mirrors node:sqlite; see client.ts.
import crypto from 'crypto';
import logger from '../lib/logger.js';
import { readSecret } from '../lib/secrets.js';
import { getDb, closeDb } from './client.js';
import { runMigrations } from './migrate.js';

export { getDb, closeDb };

/**
 * Initialize the database on startup: apply migrations, purge expired
 * soft-deleted sessions, and seed default settings. Replaces the synchronous
 * schema bootstrap that node:sqlite did inside getDb().
 */
export async function initDb(): Promise<void> {
  await runMigrations();
  await purgeExpiredSessions();
  await seedDefaultSettings();
}

// ── Purge soft-deleted sessions past the retention window (7 days) ───────────
async function purgeExpiredSessions(): Promise<void> {
  const db = getDb();
  try {
    const cutoff = Date.now() - 7 * 24 * 60 * 60 * 1000;
    const stale = (await db
      .prepare('SELECT id FROM sessions WHERE deleted_at IS NOT NULL AND deleted_at < ?')
      .all(cutoff)) as Array<{ id: string }>;
    for (const { id } of stale) {
      // session_inputs / analysis_results / analyst_notes cascade via FK, but
      // delete explicitly to match V2 behavior exactly.
      await db.prepare('DELETE FROM session_inputs WHERE session_id = ?').run(id);
      await db.prepare('DELETE FROM analysis_results WHERE session_id = ?').run(id);
      await db.prepare('DELETE FROM analyst_notes WHERE session_id = ?').run(id);
      await db.prepare('DELETE FROM sessions WHERE id = ?').run(id);
    }
    if (stale.length > 0) {
      logger.info(`Purged ${stale.length} soft-deleted session(s) past 7-day retention`);
    }
  } catch (err) {
    logger.warn({ err }, 'Session purge skipped (best-effort)');
  }
}

